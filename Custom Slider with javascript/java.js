function EhasanSlider() {
	if (document.getElementsByClassName("slider_img")[0] .classList.contains("active")){
	
	document.getElementsByClassName("slider_img")[1] .classList.add("active")
	document.getElementsByClassName("slider_img")[0] .classList.remove("active")
}
else if (document.getElementsByClassName("slider_img")[1] .classList.contains("active")){
	
	document.getElementsByClassName("slider_img")[2] .classList.add("active")
	document.getElementsByClassName("slider_img")[1] .classList.remove("active")
}
else if (document.getElementsByClassName("slider_img")[2] .classList.contains("active")){
	
	document.getElementsByClassName("slider_img")[3] .classList.add("active")
	document.getElementsByClassName("slider_img")[2] .classList.remove("active")
}

else if (document.getElementsByClassName("slider_img")[3] .classList.contains("active")){
	
	document.getElementsByClassName("slider_img")[4] .classList.add("active")
	document.getElementsByClassName("slider_img")[3] .classList.remove("active")
}
else if (document.getElementsByClassName("slider_img")[4] .classList.contains("active")){
	
	document.getElementsByClassName("slider_img")[0] .classList.add("active")
	document.getElementsByClassName("slider_img")[4] .classList.remove("active")
}};

setInterval(EhasanSlider,3000);
